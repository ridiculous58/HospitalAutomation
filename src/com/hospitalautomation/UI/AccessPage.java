    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
       Doctor doctor = new Doctor
        (
                tbxNameAdd.getText(),
                tbxSurnameAdd.getText(),
                LocalDateTime.parse(tbxBirthdateAdd.getText()),
                Integer.parseInt(tbxExpertiseIdAdd.getText())
        );
        doctorService.add(doctor);
        JOptionPane.showMessageDialog(null,"Doktor Başarıyla Eklendi");
        TableFill();
    }//GEN-LAST:event_btnAddActionPerformed

    private void tblDoctorsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDoctorsMouseClicked
        int row=tblDoctors.rowAtPoint(evt.getPoint());
        tbxIdUpdate.setText(tblDoctors.getValueAt(row,0).toString());
        tbxNameUpdate.setText(tblDoctors.getValueAt(row,1).toString());
        tbxSurnameUpdate.setText(tblDoctors.getValueAt(row,2).toString());
        tbxBirthdateUpdate.setText(tblDoctors.getValueAt(row,3).toString());
        tbxExpertiseIdUpdate.setText(tblDoctors.getValueAt(row,4).toString());
        
        tbxIdDelete.setText(tblDoctors.getValueAt(row,0).toString());
        
        
    }//GEN-LAST:event_tblDoctorsMouseClicked

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        Doctor doctor = new Doctor
        (
                Integer.parseInt(tbxIdUpdate.getText()),
                tbxNameUpdate.getText(),
                tbxSurnameUpdate.getText(),
                LocalDateTime.parse(tbxBirthdateUpdate.getText()),
                Integer.parseInt(tbxExpertiseIdUpdate.getText())
        );
        doctorService.update(doctor);
        JOptionPane.showMessageDialog(null,"Doktor Başarıyla Güncellendi");
        TableFill();
        
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        doctorService.delete(Integer.parseInt(tbxIdDelete.getText()));
        JOptionPane.showMessageDialog(null,"Doktor Başarıyla Silindi");
        TableFill();
    }//GEN-LAST:event_btnDeleteActionPerformed

