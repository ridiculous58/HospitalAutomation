    public void TableFill(){
        model.setRowCount(0);
        doctors = doctorService.getList();
        
        for (Doctor doctor :doctors ) {
            Object[] object = new Object[]{
         doctor.getId(),doctor.getName(),doctor.getSurname(),doctor.getBirthdate(),doctor.getExpertiseId()
            };
            model.addRow(object);
        }
    }

